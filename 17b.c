#include <stdio.h>
#include <unistd.h>
#include <stdlib.h>
#include <string.h>

int main(){
  int fd_1[2];
  pipe(fd_1);
  if(!fork()){
     close(1);
     close(fd_1[0]);
     dup2(fd_1[1],1);
     execlp("ls","ls","-l",(char*) NULL);
  }
  else{
     close(0);
     close(fd_1[1]);
     dup2(fd_1[0],0);
     execlp("wc","wc", (char*) NULL);
  }
  return 0;
}

