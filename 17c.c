#include <stdio.h>
#include <unistd.h>
#include <stdlib.h>
#include <string.h>
#include <fcntl.h>

int main(){
  int fd_1[2];
  pipe(fd_1);
  if(!fork()){
     close(1);
     fcntl(fd_1[1], F_DUPFD, 1);
     close(fd_1[0]);
     close(fd_1[1]);
     execlp("ls","ls","-l",(char*) NULL);
  }
  else{
     close(0);
     fcntl(fd_1[0], F_DUPFD, 0);
     close(fd_1[0]);
     close(fd_1[1]);
     execlp("wc","wc", (char*) NULL);
  }
  return 0;
}


