#include <stdio.h>
#include <stdlib.h>
#include <sys/stat.h>
#include <sys/types.h>

int main(){
   const char *pathf = "/home/apoorva-kharya/hands on list 2/myfifo4";
   mode_t fm = S_IFIFO | 0666;
   int a = mknod(pathf, fm,0);
   if(a == 0) printf("FIFO file created.\n");
   else printf("FIFO file creation failed.\n");
   return 0;
}

