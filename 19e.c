#include <stdio.h>
#include <stdlib.h>
#include <sys/stat.h>
#include <sys/types.h>

int main(){
   const char *path = "/home/apoorva-kharya/hands on list 2/myfifo5";
   mode_t f = 0666;
   int a = mkfifo(path, f);
   if(a == 0) printf("FIFO file created.\n");
   else printf("FIFO file creation failed.\n");
   return 0;
}

