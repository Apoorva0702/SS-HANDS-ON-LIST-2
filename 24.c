#include<stdio.h>
#include<sys/ipc.h>
#include<sys/msg.h>

int main(void){
    key_t key;
    int msgid;
    key = ftok("." , 'B');
    msgid = msgget(key,0666 | IPC_CREAT);
    printf("message queue key :%d\n", key);
    printf("message queue ID :%d\n",msgid);
    return 0;
}

